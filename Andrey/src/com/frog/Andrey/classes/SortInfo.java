package com.frog.Andrey.classes;

public class SortInfo {

    private int passes;
    private int swaps;


    public SortInfo(int passes, int swaps) {
        this.passes = passes;
        this.swaps = swaps;
    }


    public int getPasses() {
        return passes;
    }


    public int getSwaps() {
        return swaps;
    }

}
